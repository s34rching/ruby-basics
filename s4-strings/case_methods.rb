# String case methods

p "hello".capitalize
p "heLLO".capitalize # downcase other letters
p "Hello".capitalize
p "hello world".capitalize # capitalize only the first letter

p "hello123".upcase
p "heLLO".upcase
p "Hello!@#".upcase
p "hello world".upcase

p "hello1$".downcase
p "heLLO".downcase
p "Hello".downcase
p "HELLO WORLD".downcase

p "hello1$".swapcase
p "heLLO".swapcase
p "Hello".swapcase
p "HELLO WORLD".swapcase